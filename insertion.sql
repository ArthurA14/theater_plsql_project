-- Condition d'insert sur le type de client 
CREATE OR REPLACE TRIGGER insCustomerTicket
    BEFORE INSERT ON TICKETS
    FOR EACH ROW
DECLARE
    var_lab VARCHAR2(50); 
    priceT DECIMAL(15,2);
    insertException EXCEPTION;
BEGIN
    IF INSERTING THEN 
        SELECT statusCustomer, priceTicket INTO var_lab, priceT
        FROM TICKETS 
        WHERE idCustomer = :NEW.idCustomer;
        IF LOWER(var_lab) = 'normal' AND priceT != 25 THEN
            RAISE insertException;
        ELSIF LOWER(var_lab) = 'elderly' AND priceT != 20 THEN
            RAISE insertException;
        ELSIF LOWER(var_lab) IN ('student','unemployed') AND priceT != 15 THEN
            RAISE insertException;
        ELSIF LOWER(var_lab) = 'child' AND priceT != 10 THEN
            RAISE insertException;
        ELSIF LOWER(var_lab) NOT IN ('normal', 'elderly', 'student','unemployed', 'child') THEN 
            RAISE insertException;
        END IF; 
    END IF;
EXCEPTION 
    WHEN insertException THEN
        raise_application_error(-20007,'Insertion impossible, le prix du ticket ne correspond pas à la bonne catégorie de client');
END;

-- Modification du prix de base des tickets en fonction de la date d'achat de ceux-ci et du taux de remplissage de la salle
CREATE OR REPLACE TRIGGER insTicketsShowRepDate
    BEFORE INSERT ON TICKET_SHOW
    FOR EACH ROW
DECLARE
    rep_date DATE;
    nb NUMBER;
BEGIN
    IF INSERTING THEN
        SELECT dat, COUNT(idTicket) INTO rep_date, nb
        FROM TICKET_SHOW 
        WHERE idShow = :NEW.idShow
        AND idTheaterCompany = :NEW.idTheaterCompany
        AND idTicket = :NEW.idTicket
        GROUP BY (idShow, idTheaterCompany, dat);
        IF TO_DATE(SYSDATE, 'DD-MM-YYYY') - TO_DATE(rep_date, 'DD-MM-YYYY') <= 15 THEN
            INSERT INTO TICKET_SHOW (dat, idTicket, priceTicket, idShow, idTheaterCompany, idTheaterHall, hallCapacity)      
            VALUES(rep_date, :NEW.idTicket, 0.80 * :NEW.priceTicket, :NEW.idShow, :NEW.idTheaterCompany, :NEW.idTheaterHall, :NEW.hallCapacity); 
        ELSIF TO_DATE(SYSDATE, 'DD-MM-YYYY') - TO_DATE(rep_date, 'DD-MM-YYYY') = 0 AND nb < 0.50 * :NEW.hallCapacity THEN
            INSERT INTO TICKET_SHOW (dat, idTicket, priceTicket, idShow, idTheaterCompany, idTheaterHall, hallCapacity)      
            VALUES(rep_date, :NEW.idTicket, 0.70 * :NEW.priceTicket, :NEW.idShow, :NEW.idTheaterCompany, :NEW.idTheaterHall, :NEW.hallCapacity);
        ELSIF TO_DATE(SYSDATE, 'DD-MM-YYYY') - TO_DATE(rep_date, 'DD-MM-YYYY') = 0 AND nb < 0.03 * :NEW.hallCapacity THEN
            INSERT INTO TICKET_SHOW (dat, idTicket, priceTicket, idShow, idTheaterCompany, idTheaterHall, hallCapacity)      
            VALUES(rep_date, :NEW.idTicket, 0.50 * :NEW.priceTicket, :NEW.idShow, :NEW.idTheaterCompany, :NEW.idTheaterHall, :NEW.hallCapacity); 
        ELSE 
            INSERT INTO TICKET_SHOW (dat, idTicket, priceTicket, idShow, idTheaterCompany, idTheaterHall, hallCapacity)       
            VALUES(rep_date, :NEW.idTicket, :NEW.priceTicket, :NEW.idShow, :NEW.idTheaterCompany, :NEW.idTheaterHall, :NEW.hallCapacity);
        END IF;
    END IF;
END;

-- S'assurer que les insert dans la table REPRESENTATION ont des signes arithmétiques cohérents avec 
-- la situation de spectacle concernée : une compagnie qui accueille son propre spectacle ou qui l'exporte dans une autre salle
-- n''aura pas les mêmes dépenses ni les mêmes recettes (globalPrice) dans l’un ou l’autre des deux cas
-- (voir aussi le trigger majBudgTheaterRep ci-dessous)
CREATE TRIGGER insertExpenditure
   AFTER INSERT OR UPDATE ON REPRESENTATION
   FOR EACH ROW
DECLARE
    travelCosts DECIMAL(15,2);
    comFees DECIMAL(15,2);
    globalPrice DECIMAL(15,2);
    prodCosts DECIMAL(15,2);
    idThC NUMBER(4);
    idThHall NUMBER(4);
BEGIN
    IF INSERTING THEN
        SELECT
            travelCostsPerRep ,comFeesPerRep ,globalPrice ,prodCosts, idTheaterCompany, idTheaterHall
            INTO travelCosts, comFees, globalPrice, prodCosts, idThC, idThHall
        FROM REPRESENTATION
        WHERE idTheaterCompany = :NEW.idTheaterCompany AND idTheaterHall = :NEW.idTheaterHall;
        -- AND idShow = :NEW.idShow AND dat = :NEW.dat;
        IF idThHall = idThC THEN
            travelCosts := 0;
            comFees := - ABS(comFees);
            globalPrice := 0;
            prodCosts := - ABS(prodCosts);
        ELSIF idThHall != idThC THEN
            travelCosts := - ABS(travelCosts);
            comFees := - ABS(comFees);
            globalPrice := ABS(globalPrice);
            prodCosts := - ABS(prodCosts);
        END IF;
    END IF; 
END;

-- Mettre à jour, dans la table THEATER_COMPANY, le champ budget du théâtre accueillant la représentation d’un spectacle, après chaque insert dans la table REPRESENTATION
CREATE OR REPLACE TRIGGER majBudgTheaterRep
    AFTER INSERT ON REPRESENTATION
    FOR EACH ROW
DECLARE 
    travelCosts DECIMAL(15,2);
    comFees DECIMAL(15,2);
    globalPrice DECIMAL(15,2);
    prodCosts DECIMAL(15,2);
    idThC NUMBER(4);
    idThHall NUMBER(4);
BEGIN 
    IF INSERTING THEN
        SELECT 
        travelCostsPerRep ,comFeesPerRep ,globalPrice ,prodCosts, idTheaterCompany, idTheaterHall
        INTO travelCosts, comFees, globalPrice, prodCosts, idThC, idThHall
        FROM REPRESENTATION
        WHERE idTheaterCompany = :NEW.idTheaterCompany AND idTheaterHall = :NEW.idTheaterHall;
        IF idThHall = idThC THEN
            UPDATE THEATER_COMPANY 
            SET budget = budget + comFees + prodCosts,
            budgetMonth = SYSDATE
            WHERE idTheater = :NEW.idTheaterCompany;
        ELSIF idThHall <> idThC THEN
            UPDATE THEATER_COMPANY 
            SET budget = budget + travelCosts + comFees + globalPrice + prodCosts,
            budgetMonth = SYSDATE
            WHERE idTheater = :NEW.idTheaterCompany;
            UPDATE THEATER_COMPANY 
            SET budget = budget - globalPrice,
            budgetMonth = SYSDATE
            WHERE idTheater = :NEW.idTheaterHall;
        END IF;
    END IF;
END;

-- Alimenter la table THEATER_COMPANY après chacun des dons, qui ont lieu chaque mois
CREATE OR REPLACE TRIGGER majBudgTheaterDonation
    AFTER INSERT ON DONATION 
    FOR EACH ROW
DECLARE 
    priceDonation DECIMAL(15,2);
BEGIN 
    IF INSERTING THEN
        SELECT priceDonation INTO priceDonation
        FROM DONATION
        WHERE idTheater = :NEW.idTheater;
        UPDATE THEATER_COMPANY 
        SET budget = budget + priceDonation,
        budgetMonth = SYSDATE
        WHERE idTheater = :NEW.idTheater;
    END IF;
END;

-- Alimenter la table THEATER_COMPANY au début de chaque mois avec la  billetterie du mois précédent, 
-- intégrée dans le budget du théâtre concerné
CREATE OR REPLACE TRIGGER majBudgTheaterTickets
    AFTER INSERT ON TICKET_SHOW 
    FOR EACH ROW
DECLARE 
    sumPrice DECIMAL(15,2);
    maxDate DATE;
BEGIN 
    IF INSERTING THEN
        SELECT 
            SUM(priceTicket), max(dat)
            INTO sumPrice, maxDate
        FROM TICKET_SHOW
        WHERE idTheaterHall = :NEW.idTheaterHall
        AND MONTHS_BETWEEN(SYSDATE, dat) = 1 
        GROUP BY idTheaterHall;
        IF (maxDate <= :NEW.dat) THEN
            UPDATE THEATER_COMPANY 
            SET budget = budget + sumPrice,
            budgetMonth = SYSDATE
            WHERE idTheater = :NEW.idTheaterHall;
        END IF;  
    END IF;
END;



