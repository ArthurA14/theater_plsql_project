-- Vérifier que le client auquel est rattaché le ticket existe en BDD dans la table CUSTOMER
-- -> pas forcément nécessaire car l'enregistrement de ticket se fait automatiquement dans l''espace d'un utilisateur existant, 
-- peut être utile dans le cas de l'insert à la main de la part d'un compte administrateur
CREATE TRIGGER insertionTicket
    BEFORE INSERT ON TICKETS
    FOR EACH ROW
DECLARE 
    nb NUMBER;
    insertException EXCEPTION;
BEGIN
    SELECT COUNT(*) INTO nb
    FROM CUSTOMER
    WHERE idCustomer = :NEW.idCustomer;
    IF (nb = 0) THEN
        RAISE insertException;
    END IF;
EXCEPTION 
    WHEN insertException THEN
        raise_application_error(-20000,'Insertion impossible, le client n''existe pas');
END;

